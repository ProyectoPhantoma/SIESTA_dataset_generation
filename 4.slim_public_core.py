#!/usr/bin/env python3
"""
slim_public_core.py

Create a "public-core" slim copy of a SIESTA dataset by selecting only key files.

Typical layout in ROOT:
  ROOT/Al/cfg_0000/...
  ROOT/Fe/cfg_0000/...
  ROOT/Ni/cfg_0000/...

Inputs:
  - index_full.parquet or index_full.jsonl (generated by your indexer)

Outputs in OUT_DIR:
  - data/<element>/cfg_xxxx/<selected files>
  - index_core.jsonl
  - index_core.parquet (if pandas+pyarrow available)
  - README_CORE.txt (summary)

Copy modes:
  --mode copy      (default) physically copies files
  --mode hardlink  uses hardlinks (fast + space-saving; same filesystem required)
  --mode symlink   uses symlinks (small; not ideal for archival releases)

Examples:
  # Full public core, hardlink to save space (same disk):
  python slim_public_core.py \
    --root raw_single_elements \
    --index index_full.parquet \
    --out   public_core \
    --mode hardlink --workers 16

  # Copy and compute checksums (slow):
  python slim_public_core.py ... --checksums
"""

from __future__ import annotations

import argparse
import hashlib
import json
import os
import random
import shutil
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, Iterable, List, Optional, Tuple

# --- core file rules ---
CORE_GLOBS = [
    "*.out",
    "*.fdf",
    "*.XV",
    "*.STRUCT_OUT",
    "Rho.grid.nc",
    "ElectrostaticPotential.grid.nc",
]

# Small but useful for provenance/repro
PROV_GLOBS = [
    "*.ion",
    "*.ion.xml",
]

# Pseudos: optional (licensing!)
PSML_GLOBS = [
    "*.psml",
]


@dataclass
class CoreRow:
    calc_id: str
    element: str
    calc_dir: str  # relative to root
    scf_converged: Optional[bool]
    scf_iterations: Optional[int]
    final_energy_ev: Optional[float]
    fermi_ev: Optional[float]
    dm_read_failed: Optional[bool]
    dm_atomic_fallback: Optional[bool]
    files: Dict[str, Optional[str]]  # relative to OUT_DIR (for the slim dataset)


def sha256_file(path: Path, chunk: int = 2**20) -> str:
    h = hashlib.sha256()
    with path.open("rb") as f:
        while True:
            b = f.read(chunk)
            if not b:
                break
            h.update(b)
    return h.hexdigest()


def safe_mkdir(p: Path) -> None:
    p.mkdir(parents=True, exist_ok=True)


def link_or_copy(src: Path, dst: Path, mode: str) -> None:
    safe_mkdir(dst.parent)
    if dst.exists():
        return
    if mode == "copy":
        shutil.copy2(src, dst)
    elif mode == "hardlink":
        os.link(src, dst)
    elif mode == "symlink":
        # relative symlink for portability within out dir
        rel = os.path.relpath(src, dst.parent)
        os.symlink(rel, dst)
    else:
        raise ValueError(f"Unknown mode: {mode}")


def load_index(index_path: Path) -> List[dict]:
    """
    Load index_full.parquet if possible; else index_full.jsonl.
    Returns list of dict rows.
    """
    if index_path.suffix.lower() == ".parquet":
        try:
            import pandas as pd  # type: ignore
            df = pd.read_parquet(index_path)
            return df.to_dict(orient="records")
        except Exception as e:
            raise SystemExit(f"Failed to read parquet index ({index_path}): {e}")

    # jsonl
    rows = []
    with index_path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            rows.append(json.loads(line))
    return rows


def select_rows(rows: List[dict], elements: Optional[List[str]], limit: Optional[int], seed: int) -> List[dict]:
    if elements:
        want = set(elements)
        rows = [r for r in rows if r.get("element") in want]

    if limit is not None and limit < len(rows):
        rnd = random.Random(seed)
        rows = rows[:]  # copy
        rnd.shuffle(rows)
        rows = rows[:limit]

    return rows


def gather_files(calc_dir_abs: Path, include_prov: bool, include_psml: bool) -> List[Path]:
    globs = list(CORE_GLOBS)
    if include_prov:
        globs += PROV_GLOBS
    if include_psml:
        globs += PSML_GLOBS

    files: List[Path] = []
    for g in globs:
        files.extend(sorted(calc_dir_abs.glob(g)))
    # de-dup
    uniq = []
    seen = set()
    for p in files:
        if p not in seen and p.is_file():
            uniq.append(p)
            seen.add(p)
    return uniq


def worker_task(args: Tuple[str, str, str, bool, bool, bool]) -> Tuple[str, List[Tuple[str, str]], List[str]]:
    """
    Returns:
      calc_dir_rel,
      copied list of (src_rel_to_root, dst_rel_to_out),
      missing list of filenames/globs (best-effort)
    """
    root_str, out_data_str, calc_dir_rel, include_prov, include_psml, mode = args
    root = Path(root_str)
    out_data = Path(out_data_str)

    calc_abs = root / calc_dir_rel
    files = gather_files(calc_abs, include_prov=include_prov, include_psml=include_psml)

    copied: List[Tuple[str, str]] = []
    missing: List[str] = []

    # basic missing checks for the MUST-HAVEs:
    must = ["Rho.grid.nc", "ElectrostaticPotential.grid.nc"]
    for m in must:
        if not (calc_abs / m).exists():
            missing.append(m)

    # Map to OUT_DIR/data/<calc_dir_rel>/same filenames
    for src in files:
        src_rel = str(src.relative_to(root))
        dst = out_data / calc_dir_rel / src.name
        link_or_copy(src, dst, mode=mode)
        dst_rel = str(dst.relative_to(out_data.parent))  # relative to OUT_DIR
        copied.append((src_rel, dst_rel))

    return calc_dir_rel, copied, missing


def write_jsonl(rows: List[CoreRow], path: Path) -> None:
    with path.open("w", encoding="utf-8") as f:
        for r in rows:
            f.write(json.dumps(r.__dict__, ensure_ascii=False) + "\n")


def write_parquet_if_possible(rows: List[CoreRow], path: Path) -> bool:
    try:
        import pandas as pd  # type: ignore
        import pyarrow  # noqa: F401
    except Exception:
        return False

    # flatten files dict
    flat = []
    for r in rows:
        d = dict(r.__dict__)
        files = d.pop("files")
        for k, v in files.items():
            d[k] = v
        flat.append(d)

    df = pd.DataFrame(flat)
    df.to_parquet(path, index=False, engine="pyarrow")
    return True


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--root", required=True, type=str, help="Original dataset root")
    ap.add_argument("--index", required=True, type=str, help="index_full.parquet or index_full.jsonl")
    ap.add_argument("--out", required=True, type=str, help="Output directory for slim dataset")
    ap.add_argument("--mode", choices=["copy", "hardlink", "symlink"], default="copy")
    ap.add_argument("--workers", type=int, default=8)
    ap.add_argument("--elements", nargs="*", default=None, help="Subset elements, e.g., --elements Ni Fe")
    ap.add_argument("--limit", type=int, default=None, help="Limit number of calcs (after filtering)")
    ap.add_argument("--seed", type=int, default=0)
    ap.add_argument("--include-provenance", action="store_true", help="Include *.ion and *.ion.xml")
    ap.add_argument("--include-psml", action="store_true", help="Include *.psml (check redistribution license!)")
    ap.add_argument("--checksums", action="store_true", help="Compute sha256 for copied files (slow)")
    args = ap.parse_args()

    root = Path(args.root).resolve()
    index_path = Path(args.index).resolve()
    out_dir = Path(args.out).resolve()

    out_data = out_dir / "data"
    safe_mkdir(out_data)

    # Load + select
    rows_raw = load_index(index_path)
    rows_raw = select_rows(rows_raw, elements=args.elements, limit=args.limit, seed=args.seed)

    print(f"Loaded index rows: {len(rows_raw)} selected for slim copy.")
    print(f"Root: {root}")
    print(f"Out:  {out_dir}")
    print(f"Mode: {args.mode}  workers={args.workers}")
    print(f"Include provenance: {args.include_provenance}")
    print(f"Include psml:       {args.include_psml}")

    # Prepare tasks
    tasks = []
    for r in rows_raw:
        calc_dir_rel = r["calc_dir"]  # like "Fe/cfg_2335"
        tasks.append((
            str(root),
            str(out_data),
            calc_dir_rel,
            bool(args.include_provenance),
            bool(args.include_psml),
            args.mode,
        ))

    # Execute (multiprocessing)
    from multiprocessing import Pool

    copied_total = 0
    missing_total = 0
    missing_examples: List[Tuple[str, List[str]]] = []

    core_rows: List[CoreRow] = []
    checksum_lines: List[str] = []

    with Pool(processes=args.workers) as pool:
        for calc_dir_rel, copied, missing in pool.imap_unordered(worker_task, tasks, chunksize=20):
            copied_total += len(copied)
            if missing:
                missing_total += 1
                if len(missing_examples) < 20:
                    missing_examples.append((calc_dir_rel, missing))

            # Build a slim index row pointing into OUT_DIR
            # (We keep scalar fields from original index)
            src = next(rr for rr in rows_raw if rr["calc_dir"] == calc_dir_rel)

            # Map file pointers to slim dataset
            # We'll use stable keys for core pointers:
            #   out_path, fdf_path, xv_path, struct_out_path, rho_nc_path, vh_nc_path
            # plus optional provenance and psml pointers if included
            filemap: Dict[str, Optional[str]] = {}
            # Look up what we copied (dst_rel_to_out is relative to OUT_DIR)
            dst_by_name = {Path(dst).name: dst for _src, dst in copied}

            def pick(name_glob: str, key: str) -> None:
                # pick first match from copied names by suffix/ exact
                if name_glob.startswith("*"):
                    suf = name_glob[1:]
                    for fn, dst in dst_by_name.items():
                        if fn.endswith(suf):
                            filemap[key] = dst
                            return
                    filemap[key] = None
                else:
                    filemap[key] = dst_by_name.get(name_glob)

            pick(".out", "out_path")
            pick(".fdf", "fdf_path")
            pick(".XV", "xv_path")
            pick(".STRUCT_OUT", "struct_out_path")
            pick("Rho.grid.nc", "rho_nc_path")
            pick("ElectrostaticPotential.grid.nc", "vh_nc_path")
            if args.include_provenance:
                pick(".ion", "ion_path")
                pick(".ion.xml", "ion_xml_path")
            if args.include_psml:
                pick(".psml", "psml_path")

            core_rows.append(CoreRow(
                calc_id=src.get("calc_id"),
                element=src.get("element"),
                calc_dir=calc_dir_rel,
                scf_converged=src.get("scf_converged"),
                scf_iterations=src.get("scf_iterations"),
                final_energy_ev=src.get("final_energy_ev"),
                fermi_ev=src.get("fermi_ev"),
                dm_read_failed=src.get("dm_read_failed"),
                dm_atomic_fallback=src.get("dm_atomic_fallback"),
                files=filemap,
            ))

            if args.checksums:
                # compute checksums for this calc's copied files
                for _src_rel, dst_rel in copied:
                    dst_abs = out_dir / dst_rel
                    try:
                        checksum_lines.append(f"{sha256_file(dst_abs)}  {dst_rel}")
                    except OSError:
                        checksum_lines.append(f"ERROR  {dst_rel}")

    # Write slim indexes
    index_jsonl = out_dir / "index_core.jsonl"
    write_jsonl(core_rows, index_jsonl)
    print(f"Wrote: {index_jsonl} ({len(core_rows)} rows)")

    index_parquet = out_dir / "index_core.parquet"
    if write_parquet_if_possible(core_rows, index_parquet):
        print(f"Wrote: {index_parquet}")
    else:
        print("Parquet not written (missing pandas+pyarrow). JSONL is ready.")

    # Write checksums if requested
    if args.checksums:
        chk = out_dir / "SHA256SUMS"
        chk.write_text("\n".join(checksum_lines) + "\n")
        print(f"Wrote: {chk}")

    # Write summary
    summary = out_dir / "README_CORE.txt"
    txt = []
    txt.append("Public-core slim dataset created by slim_public_core.py")
    txt.append(f"Original root: {root}")
    txt.append(f"Index used:    {index_path}")
    txt.append(f"Output dir:    {out_dir}")
    txt.append(f"Rows selected: {len(rows_raw)}")
    txt.append(f"Files copied/linked: {copied_total}")
    txt.append(f"Calcs with missing MUST files (rho/vh): {missing_total}")
    if missing_examples:
        txt.append("\nExamples of missing MUST files:")
        for d, m in missing_examples:
            txt.append(f"  {d}: {m}")
    summary.write_text("\n".join(txt) + "\n")
    print(f"Wrote: {summary}")


if __name__ == "__main__":
    main()